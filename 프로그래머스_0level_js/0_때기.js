/**
 * 중간 점검 - > 어떻게 해야 배열 가장 왼쪽에 있는 0만 제거 할수 있을까?
 * 배열의 길이 기준 / 2 에서 멈춰야 함.
 * 
 * filter 메서드에서 어떻게 이런 조건으로 원하는 값을 만들수 있는가?
 * filter에서는 원하는 index 까지 조절하는 부분이 없는것 같다....;;
 * 
 * 그외 다른 메서드는 없을까? 
 * 
 * - repace 정규표현식? 정규표현 식을 통해 문자를 재배치 하고, 
 * replace("찾을 문자열","변경할 문자열이나 값") 의 형식이다.
 * 여기에 찾을 문자열에 정규 표현식을 쓰면 손쉽게 찾을수 있다.
 */

function solution (n_str) {
    return [...n_str].replace( /^0+/ , '');
}

/**
 * replae 메서드와 정규 표현식을 쓰니 생각보다 쉽게 해결 되었다.
 * 
 * replace -> 어떤 문자열을 원하는 문자열로 대체할수 있게 해주는 메서드, 문자형 자료를
 *            변경할때 쓰인다.
 * 
 * 정규 표현식 ->  /^0+/ "^" 부분은 문자열의 시작을 나타내는 특수 문자.
 *                0+ 는 0의 하나 이상 이다 라는 것을 의미함.
 */
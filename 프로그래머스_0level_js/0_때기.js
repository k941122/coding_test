/**
 * 0 때기
 * 
 * 문제 설명
 * 
 * 정수로 이루어진 문자열 n_str이 주어질 때, 
 * n_str의 가장 왼쪽에 처음으로 등장하는 0들을 뗀 문자열을 
 * return하도록 solution 함수를 완성해주세요.
 * 
 * 제한 사항
 * - 2 ≤ n_str ≤ 10
 * - n_str이 "0"으로만 이루어진 경우는 없습니다.
 * 
 * 입출력 예시
 * 
 *  n_str	 result
 * "0010"	  "10"
 * "854020"	 "854020"
 * 
 * 입출력 예시 설명
 * 
 * ex1) "0010"의 가장 왼쪽에 연속으로 등장하는 "0"을 모두 제거하면 "10"이 됩니다.
 * ex2) "854020"는 가장 왼쪽에 0이 없으므로 "854020"을 return합니다.
 * 
 */

/**
 * 문제 해석
 * 
 * n_str =정수로 이루어진 문자열 "1567"=  [1,5,6,7,] = 
 * 정수형 배열로 바꾸기 [...n_str]
 * 
 * 가장 왼쪽의 0들을 때고 -> 특정 조건(가장 왼쪽의 0들 제거)전체 길이/2 이전 -> 
 * filter (() => )
 * 
 * 정수 return -> 나온 배열을 .join('')으로 묶어주기
 * 
 */

// ▼ 오류 코드 01
function solution (n_str) {
    let arr = [...n_str];
    return arr.filter((ele) => ele === 0? ele :"" ).join("")
}

